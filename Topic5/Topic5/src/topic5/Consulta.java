    public void getFinalNotes(int student){
        String query = "Select nc.student, course_name, final from notesxcourse nc join student s "
                + "on nc.student=s.reg_number and s.reg_number="+student+" order by nc.final desc ";
        String result = "Final notes for student number "+student+"\n";
     try {
         st = cn.createStatement();
         rs = st.executeQuery(query);
         while (rs.next()) {

				String course = rs.getString(2);
				int finalNote = rs.getInt(3);
				 result += (course + " " + finalNote+"\n");
     }
     }catch (SQLException ex) {
         Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
     }
     System.out.println(result);
    }
    public void getStudentsXCourse(){
         String course = cmb_course.getSelectedItem().toString();
        String str = "Course name: " + course + "\n";
         String student="";
         String teacher = "";
       try {
           
           
            st = cn.createStatement();
            String query = "Select s.first_name, s.last_name, c.name, t.first_name,"
                    + "t.last_name from course c JOIN studentxcourse sc on "
                    + "sc.course_name = c.name join teacher t on t.license = c.teacher "
                    + "join student s on s.reg_number = sc.student and c.name = "+"'"+course+"' "
                    + "order by s.last_name";
            rs = st.executeQuery(query);
            
          
            while (rs.next()){
               
                student += rs.getString(2) +" "+ rs.getString(1)+ "\n";
                teacher = rs.getString(5) +" "+ rs.getString(4);
            
            }
            String res = str + "Teacher: " + teacher + "\n" +
                    "Students: "+ "\n" +student;
           System.out.println(res);
         //   System.out.println(teacher);
        } catch (SQLException ex) {
            System.out.println("Imposible realizar conexion con "+server+" ... FAIL");
            //Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex)
        }
        
    } 
    
